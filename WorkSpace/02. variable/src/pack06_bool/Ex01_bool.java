package pack06_bool;

public class Ex01_bool {
	public static void main(String[] args) {
		//부울형(논리형) : boolean
		// true , false 만 기억 ▶ 조건식 작성 시 사용.
		//컴퓨터는 어떠한 판단을 true false로만 함
		// 컴퓨터에게 어떠한 질문을 할때는 true , false로 답이 나오는것만 가능.
		// 예시) 내이름이 뭐게? x
		// 오늘 온도가 높은지? x
		// 오늘 온도가 15도 보다 높습니까? o
		boolean t = true ; //데이터 초기화 식
		boolean f = false ;
		// 문자열도 표현하고 싶고 변수도 표현하고싶다?
		System.out.println("변수 t에 들어가는 값 : " + t);
		//변수 f에 들어있는 값? 표현해보기
		System.out.println("변수 f에 들어가는 값 : " + f);
	}
}
